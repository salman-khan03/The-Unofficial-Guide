"""
run_eval.py — Milestone 6 evaluation harness.

Runs the system on the 5 planning.md evaluation questions and writes a markdown
report (evaluation_results.md) capturing, per question: the question, retrieved
chunks with distances, the system's answer, and the cited sources. Accuracy
judgments are left for you to fill in after reviewing the output.

    python run_eval.py
"""

from pathlib import Path

from query import ask

# (question, expected answer) — keep in sync with planning.md Evaluation Plan.
EVAL = [
    ("What do students say about Andrew Ng's machine learning courses?",
     "Highly regarded; top researcher, explains ML clearly, difficult but worth it; "
     "Coursera courses praised."),
    ("How do students describe Percy Liang as a professor?",
     "Polarizing, low-rated (2.7/5); difficult to talk to / dismissive in office hours "
     "and confusing in CS336, though a minority praise his lecturing."),
    ("Do students find David Malan's CS50 lectures clear, or too lecture-heavy?",
     "Mixed: some praise clear, engaging lectures; others say lecture-heavy / not much "
     "substance / too dense."),
    ("Which CS professor is described as funny or like a stand-up comedian?",
     "Chris Gregg (explicitly 'could be a stand-up comedian'); Piech also called "
     "'hilarious'."),
    ("Do any reviews mention group projects or team assignments?",
     "Not meaningfully covered — system should refuse ('I don't have enough "
     "information on that.')."),
]


def main():
    out = ["# Evaluation Results\n",
           "_Generated by run_eval.py. Fill in the **Accuracy** judgment per question "
           "after reviewing._\n"]
    for i, (q, expected) in enumerate(EVAL, 1):
        print(f"Running Q{i}: {q}")
        result = ask(q)
        out.append(f"\n## Q{i}. {q}\n")
        out.append(f"**Expected:** {expected}\n")
        out.append(f"**System answer:** {result['answer']}\n")
        srcs = ", ".join(result["sources"]) if result["sources"] else "(none — refused)"
        out.append(f"**Sources cited:** {srcs}\n")
        out.append("**Retrieved chunks:**\n")
        for c in result["chunks"]:
            md = c["metadata"]
            out.append(f"- `[dist {c['distance']:.3f}]` {md['source']} — "
                       f"{c['text'][:140]}{'...' if len(c['text']) > 140 else ''}")
        out.append("\n**Accuracy:** _TODO: accurate / partially accurate / inaccurate_\n")

    path = Path(__file__).parent / "evaluation_results.md"
    path.write_text("\n".join(out), encoding="utf-8")
    print(f"\nWrote {path}")


if __name__ == "__main__":
    main()
